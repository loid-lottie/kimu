const buttons = document.querySelectorAll(".genres button");
const sections = document.querySelectorAll(".genre-section");

buttons.forEach(button => {

    button.addEventListener("click", () => {


        buttons.forEach(btn => btn.classList.remove("active"));


        button.classList.add("active");

        const filter = button.dataset.filter;

        sections.forEach(section => {

            if (filter === "all") {
                section.style.display = "block";
            }
            else if (section.dataset.genre === filter) {
                section.style.display = "block";
            }
            else {
                section.style.display = "none";
            }

        });

    });

});

const movieCards = document.querySelectorAll(".movie-card");
const modal = document.getElementById("movieModal");
const closeBtn = document.querySelector(".close");

movieCards.forEach(card => {

    card.addEventListener("click", function (e) {

        e.preventDefault();

        const id = card.dataset.movie;
        const movie = movies[id];

        document.getElementById("modalPoster").src = movie.poster;
        document.getElementById("modalTitle").textContent = movie.title;
        document.getElementById("modalGenre").textContent = movie.genre;
        document.getElementById("modalSynopsis").textContent = movie.synopsis;
        document.getElementById("modalTrailer").href = movie.trailer;

        modal.style.display = "flex";

    });

});

closeBtn.addEventListener("click", function () {
    modal.style.display = "none";
});

window.addEventListener("click", function (e) {
    if (e.target === modal) {
        modal.style.display = "none";
    }
});

const searchInput = document.getElementById("searchInput");

searchInput.addEventListener("input", function () {

    const search = this.value.toLowerCase();

    sections.forEach(section => {

        const cards = section.querySelectorAll(".movie-card");
        let hasVisibleMovie = false;

        cards.forEach(card => {

            const title = card.querySelector("h3").textContent.toLowerCase();

            if (title.includes(search)) {
                card.style.display = "block";
                hasVisibleMovie = true;
            } else {
                card.style.display = "none";
            }

        });

        // Hide the whole genre if no movies match
        section.style.display = hasVisibleMovie ? "block" : "none";

    });

});